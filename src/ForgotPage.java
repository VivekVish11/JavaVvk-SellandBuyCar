    public void setSecQ(String mob,JTextField jtf){
        try{           
            String sql1="select SecQ from Account where MobileNo='"+mob+"' ";
            pst = conn.prepareStatement(sql1);
            rs = pst.executeQuery();
            if(rs.next()){
                String SQ =rs.getString("SecQ");
                jtf.setText(SQ);                          
            }
            else{
                JOptionPane.showMessageDialog(null,"Error");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        finally {
            try {
                rs.close();
                pst.close();
            }
            catch (Exception e) {
            }
        }
    }
    
