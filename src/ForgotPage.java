    public void goFindUserID(){
        try{
            String mob=Mob1.getText();           
            String seca=SecA1.getText();
            String sql1="select UserID,SecA from Account where MobileNo='"+mob+"' ";
            pst=conn.prepareStatement(sql1);
            rs = pst.executeQuery();
            if(rs.next()){                                          
                String sa =rs.getString("SecA");               
                if(seca.equals(sa)){                 
                    String uid =rs.getString("UserID");
                    JOptionPane.showMessageDialog(null, " Your UserID is: "+uid+"");
                    Mob1.setText("");
                    SecQ1.setText("");
                    SecA1.setText(""); 
                }
                else{
                    JOptionPane.showMessageDialog(null,"Incorrect Answer ");
                    Mob1.setText("");
                    SecQ1.setText("");
                    SecA1.setText(""); 
                }
            }           
        }
        catch(Exception e){       
        }
        finally {
            try {
                rs.close();
                pst.close();
            }
            catch (Exception e) {
            }
        }
    }
    
    public void ChangePassward(){
        try {
            String mob= Mob2.getText();
            String seca= SecA2.getText();
            String passward= Passward.getText();
            String Cpassward= Cpassword.getText();
            String sql1="select * from Account where MobileNo='"+mob+"' ";
            pst =conn.prepareStatement(sql1);
            rs=pst.executeQuery();
            if(rs.next()){
                String sa=rs.getString("SecA");                          
                if(passward.equals(Cpassward)){
                    if(seca.equals(sa)){                       
                        rs.close();
                        pst.close();
                        String sql2="update Account set Passward='"+passward+"'"
                                + " where MobileNo='"+mob+"' ";                           
                        pst = conn.prepareStatement(sql2);
                        pst.execute();
                        JOptionPane.showMessageDialog(null, "Passward Change Suceessfully");
                        Mob2.setText("");
                        SecQ2.setText("");
                        SecA2.setText(""); 
                        Passward.setText(""); 
                        Cpassword.setText(""); 
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Incorrect Answer"); 
                        SecA2.setText(""); 
                        Passward.setText(""); 
                        Cpassword.setText(""); 
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Re-Enter Passward");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Incorrect Details");
                Passward.setText(""); 
                Cpassword.setText(""); 
            }            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        finally {
            try {
                rs.close();
                pst.close();
            }
            catch (Exception e) {
            }
        }
    }
    
